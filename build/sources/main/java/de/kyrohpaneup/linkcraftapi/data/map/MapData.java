package de.kyrohpaneup.linkcraftapi.data.map;

public class MapData {

    String star = "N/A";
    int player = 0;
    int total = 0;

    public String getStar() {
        return star;
    }

    public void setStar(String star) {
        this.star = star;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getPlayer() {
        return player;
    }

    public void setPlayer(int player) {
        this.player = player;
    }
}
